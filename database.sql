CREATE DATABASE IF NOT EXISTS task_manager CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE task_manager;

CREATE TABLE IF NOT EXISTS tasks (
    id INT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(255) NOT NULL COMMENT 'Название проекта',
    description TEXT COMMENT 'Описание проекта, технологии, цели',
    category VARCHAR(100) DEFAULT 'веб-разработка' COMMENT 'Категория проекта',
    complexity ENUM('легко', 'средне', 'сложно') DEFAULT 'средне' COMMENT 'Сложность реализации',
    status ENUM('не выполнена', 'выполнена') DEFAULT 'не выполнена' COMMENT 'Статус: идея или реализовано',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата добавления идеи'
);

